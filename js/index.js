function reverseString(str) {

    // empty string
    let newString = "";
    for (let i = str.length - 1; i >= 0; i--) {
        newString += str[i];
    }
    return newString;
}
function formatedMoney(money){
    let m = money.toString();
    let temp = "";
    if(m.length==5)
    {
        for(let i=0;i<5;i++)
        {
            if(i==2)
                temp+=',';
            temp+=m[i];
        }
        return temp;
    }
    else if(m.length>5)
    {
        for(let i=m.length-1;i>=0;i--)
        {
            if(i==m.length-4 || i==m.length-6)
                temp+=',';
            temp+=m[i];
        }
        temp = reverseString(temp);
        return temp;
    }
    else 
        return m;
}

